			<h3>About the Goods and Services Act</h3>
			
			<p>As per the GST Act, when the goods are transported from one place to another a bill is created which is known as an E-way bill or electronic way bill. An e-way bill is an electronic bill or a document produced for recording the movement of goods from one place to another which are transported either within the state or between two or more states. However such a bill needs to be made only when the value of the goods exceeds INR 50,000 as per the GST regime. The e-way bill is to be generated by the consignor or the consignee himself but in case of neither of them do so, the transporter of the goods shall generate such a bill.</p>
			
			<p>However, if we throw some light on the validity of an e-way bill it depends upon the distance to be travelled by the goods, i.e., for a distance of less than 100 km, the e-way bill shall be valid for a day from the relevant date. On the other hand, with every 100 km afterwards the validity will be additional one day from the relevant date. Though, the validity of the e-way bill cannot be extended but the commissioner may extend such validity by issuing notification. The purpose of issuing an e-way bill is to keep the track of movement of goods from one place to another and serving as a tool to check any tax evasion.</p>
