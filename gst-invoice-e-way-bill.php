			<p>GST Invoice can be termed as an important document in the process of collecting tax under the GST Act; it is referred as an instrument which is issued by a seller or a supplier of goods or services to the buyer or the recipient of such goods or services. In more practical terms, it is an invoice issued by a registered taxable person when he supplies taxable goods or services to its customers, such an invoice works as a legit document for claiming Input tax credit (ITC) by the customers of such goods or services. The GST invoice serves as legal evidence in case of any payment being due on the part of the buyer, the seller can ask for such payment from the buyer on the basis of a GST invoice. As per the GST Act it is mandatory to issue an invoice whenever the supplier supplies any goods or services to its customers. However, it is not considered important that the invoice should be issued by the person making the supply; the GST law permits any registered person who buys goods or services from an unregistered person to issue such an invoice.</p>
			
			<p>Keeping a GST invoice imparts benefits not only to the seller but also to the buyer ,i.e., it serves as a proof of the transaction executed between both the parties, the invoice helps in maintaining the record of the time of the supply of goods or services and furthermore the buyer can claim input tax credit through this invoice.</p>
