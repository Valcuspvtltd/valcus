            <h4><strong>What is society?</strong></h4>
            
            <p>A society is a collaborative group of numerous persons cooperative utilizing shared purposeful regulate and operate jointly for some collective reason. Societies are generally registered for the development of benevolent services in the field of education, culture, art, music, sports, etc.</p>
            
            <br/>
            
            <h4><strong>What is the purpose of Society Registration? </strong></h4>
            
            <p>Societies are governed for the sponsorship for the fine arts, science, and literature and the distribution of beneficial information/political education or benevolent motive. Section 20 of the principal Act denotes the following goals for which society registration needs to be done under the Act.</p>
