            <h4><strong>Registrar of Societies</strong></h4>
            <p>Every state government employs a Registrar or Assistant Registrar of Societies who are certified to register society under the Society Registration Act, 1960.</p>
            
            
            <br/>
            <h4><strong>Selection of Name</strong></h4>
            
            <p>While selecting a name, keep in mind that name should unique for the purpose of society registration. The Societies Registration Act, 1860, does not allow registration of society with the already existing name. The name of the society must not be similar to the name of any country, state, and local administration, which is illegal under the provisions of the Emblems and Names Act, 1950.</p>
            
            <br/>
            
            <h4><strong>Memorandum of Association</strong></h4>
            
            <p>The Memorandum of Association and Rules and Regulation mandatorily need to be registered. The Memorandum of Association needs to be signed by 7 or more members, and it must also be dated. The memorandum of association has to be documented with the Registrar of Societies. The memorandum of association comprises the below-mentioned requirements.</p>
            
            <ul>
                <li>•	The name of the society</li>
                <li>•	The purpose of the society</li>
                <li>•	The names, addresses, and profession of the council, committee, directors, governors, or other governing councils where the management operation of the society is transmitted under the rules of the society</li>  
                <li>•	The rules and regulations copy of the society is authorized to be acceptable by a minimum of 3 members of the governing council should be submitted with the memorandum of association.</li>
            </ul>
            
