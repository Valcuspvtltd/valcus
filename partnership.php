            <h4><strong>Partnership</strong></h4>
            
            <p>A partnership is a contractual connection between individuals who have accepted to distribute the profits of the business obtained on by everyone involved in all, as described in Section 4 of the Indian Partnership Act. Three important components of the Partnership are as follows:</p>
			
			<p>A partnership is a contract between two or more individuals</p>
            <p>The contact must be prepared to share the profits carried away from the business</p>
            <p>The organization or business must be handled or managed by everyone or anyone of them representing all  </p>
            <p>All the above conditions must be followed before form a partnership firm</p>
            
