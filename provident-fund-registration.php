			<p class="black">Provident fund Registration</p>

			<p>Employees Provident Fund [EPF] is a scheme for the benefit of workers or Employees.’ Provident Funds and Miscellaneous Provisions Act, 1952. It is regulated by Employees’ Provident Fund Organisation (EPFO) which is the World’s largest social insurance Organizations in terms of an employee covered and also the volume of transactions. EPF is a benefit to the employee after retirement by the organization. EPF or Employees Provident Fund is mandated for an organization to offer its employees that have more than 20 employees. EPF registration is essential for companies filing the income tax return, professional tax is allowed as a deduction from your source income. We help organizations to register for provident fund.</p>
			<br/>
			<p class="black">Consulting before Application</p>
			<p>We always recommend to first consult a Provident fund expert before starting the registration process. It will save the company from needless legal tangles in the later stage. Also, it will eliminate the compliance cost for companies.</p>
			<br/>
			<p class="black">Eligibility for EPF Registration</p>
			<p>Below are the eligibility criteria for PF Registration, Company must have followed in place:</p>
			<ul>
				<li>Organizations that have 20 or more people.</li>
				<li>Salary up to Rs. 15,000 at the time of joining of an employee is required to become a member.</li>
				<li>In this act, Salary implies and includes Basic + Allowances, Cash worth of food concession and retaining grants, if any.</li>
				<li>The company and the workers of an establishment mutually decide to apply for PF to the Central PF Commissioner.</li>
			    <li>The company can also opt for voluntary registering if the worker’s total is less than 20. Each worker is entitled to PF right from the start of his employment. The responsibility of PF input and deduction is of the company’s.</li>
			</ul>
