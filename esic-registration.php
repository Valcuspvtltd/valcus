            <h4><strong>What is Employee State Insurance Corporation or ESIC Registration?</strong></h4>

			<p>As mentioned above ESIC is stands for Employees’ State Insurance Corporation. : ESIC is a scheme for workers getting a salary of Rs. 21000 or less per month. It’s self-financing health insurance endorsed by the legislature. The government of India has announced many schemes under the Employees’ State Insurance Act, 1948 to provide medical as well as cash assistance to employees and their families. As per the requirements of the Employees’ State Insurance Act, 1948, it is compulsory for all the businesses with ten or more than ten workers to obtain ESIC registration.</p>
			
			<br/>
			
			<h4><strong>Contributions of entities to the ESI</strong></h4>
			
			<p>whether it’s an employer or employee, both are essential to contribute to ESI to be eligible for avail the reimbursements provided under the scheme.</p>
			
			<br/>
			
			<h4><strong>The contributions made by them are as follows:</strong></h4>
			
			<h5>Contribution of Workers:</h5>
            <p>Employees need to pay 1.75% of their salary to the government. However, from 1st July 2019, the ESI input will reduce to 0.75%.</p>
            <h5>Contribution of Companies</h5>
            <p>The total contribution from the company should be 4.75%, which is going to be reduced to 3.25% from 1st July 2019.</p>
            
            <br/>
            
            <h4><strong>For which establishments, ESI registration is mandatory?</strong></h4>
            <p>Please find below establishments for which ESI registration is mandatory. They are as follows:</p>
            
            <ul>
                 <li>• Shops</li>
                 <li>• Cinemas</li>
                 <li>• Restaurants or Hotels providing facilities but not involved in any manufacturing business.</li>
                 <li>• Roadside Motor Transport Establishments</li>
                 <li>• Newspaper Establishments</li>
                 <li>• Medical Institutions</li>
                 <li>• Educational Institutions (Private)</li>
            </ul>
            
            <br/>
            
            <h4><strong>Advantage of obtaining ESIC online registration in India</strong></h4>
            
            <p>In general, group health insurance covers comprehensive hospitalisation costs such as room charges, doctor’s fees & nursing fees, medical equipment & appliances cost, medicine cost for employees and the members of his/her family. The policy can also be modified to add maternity benefits, higher room-rent limits, waiver of waiting periods on various diseases, baby day 1 cover, Out-patient expenses (OPD), etc as well. Individual health policies offer maternity assistance have a waiting period of 2-3 years. Under group ESIC insurance this condition of the waiting period can be waived off compare to in normal insurance. The best part is that the insured can claim the maternity benefit from Day 1 itself.</p>
            
            <p>ESIC was announced to provide benefits to the workers working under specific institutions or businesses.</p>
            
            <br/>
            
            <h4><strong>Some of them are as follows:</strong></h4>
            
            <ul>
                <li>• Medical facilities to the employee and to the members of his/her family;</li>
                <li>• Maternity benefits to pregnant women (paid leaves)</li>
                <li>• In any year, if the employee has any certified illness lasting for a maximum of 91 days, then he will be provided with sickness benefits at the rate of 70%.</li>
                <li>• If the employee dies during the working hour, then 90% of his/her salary would be given to his/her dependent every month.</li>
                <li>• The same applies in the case of any disability.</li>
                <li>• Medical expenses for old-age care.</li>
                <li>• Funeral expenses.</li>
            </ul>
            
