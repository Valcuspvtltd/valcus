                <li>•	Affidavit from directors</li>
                <li>•	PAN card and canceled cheque
                <li>•	Photo, ID and address proof of directors</li>
                <li>•	Bio-data of the competent person</li>
                <li>•	Appointment letter of a competent person</li>
               <li> •	Qualification and experience certificate of a competent person</li>
                <li>•	Photo, ID, and address of the competent person.</li>

			</ul>
			
			<h3>Types of Drug License </h3>
			
			<p>Multi-Drug License: License allotted to an organization that established pharmacies in multiple states</p>
			
			<p>Import License: License allotted to grant the import of drugs and medicines  </p>
			
			<p>Manufacturing License: License allotted to the firms that manufacture drugs and medicines including homeopathy/ allopathic</p>
			<p>Sale License: License that is allotted for the sale of drugs, this license is also classified in three categories</p>
			
			<p><strong>•	Restricted Drugs</strong></p>
			<p><strong>•	Retail Drug License</strong></p>
			<p><strong>•	Wholesale Drug License</strong></p>
			
			<p>Loan License: License allotted to the firm that doesn’t have its own manufacturing unit but utilizes the manufacturing operation of other licensed entities.</p>
			
