			<p>The Act which administrates sequential procedure right from manufacturing to the supplying or distributing of drugs in India is known as Drugs and Cosmetics Act, 1940. The Act ensures that the drugs and cosmetics sold in India are safe, effective and conform to the quality standards and that everyone who deals in drugs and medicines has fair access to it, the government of India regulates the Act through obstinate rules and guidelines so that the right to manufacture and sell drugs and medicines is observed cautiously. Though the Act has undergone a number of amendments over the several years the main objective remains the same.</p>
			
			<h3>Meaning of Drugs </h3>
			
			<p>The term “drug” as defined in the Act includes a wide variety of substance, diagnostic, and medical devices. The Act defines “cosmetic” as any product to be applied to the human body for the purpose of beautifying and cleansing. Two types of drug licenses are applicable here  (1) Wholesale license (2) Retail License for drug supplying and trading in India. </p>
			
			<p>This license is issued based on certain conditions attached to premises and the eligible person who shall be dealing with drugs. If any person using or consuming drugs gets affected due to the negligence of the manufacturer of such drugs, such an act is an offense under IPC.</p>
			<p>The documents required for filing an application for drug license include:</p>
			
			<ul>
			    
			    <li>•	Certificate of incorporation or registration</li>
