			<p class="black">Import Export Code / IE Code Registration</p>

			<p>In this age of opportunities, everyone wants to grow their business outside the boundaries of the domestic market. But, doing business worldwide isn’t just a cup of tea for everyone. Before going global, There are several procedures needs to be followed and there are laws in place, Needs to get different registration and license. IEC (Import Export Code) license is one of the most important documents when you’re planning of importing or exporting goods from India.</p>

			<p>IEC – Import Export Code 10-digit code that a business or a person needs to import/export goods/services. DGFT – Director General of Foreign trade issue this code which is under the Ministry of Commerce and Industries, Government of India. This code comes with a validity of lifetime so there is no need to renew it. Once the documents are submitted this code is get generated within 5-15 working days with all the corrections.</p>

			<p>This code can be defined as an import-export number, IE code, importer exported code, import-export code, import-export license, IE license, etc. This code can be issued under your company’s name or you can get this under your name. The best part is that once this code is issued, it is valid for all its division/units/ branches/factories.</p>

			<br/>

			<p class="black">When and Why the IEC code required?</p>

			<p>1. If you are an importer It is a mandate document required by the customs authorities to clear the shipments.</p>
			<p>2. In the case of any imports made It is needed by the bank to transfer the amount.</p>
			<p>3. It is required if an exporter needs to send the shipments.</p>
			<p>4. The exporter can get foreign currency transferred directly into his bank account if he has Import Export Code</p>
			<br/>

			<p class="black">Benefits of IEC Code</p>

			<p class="black">Growth in Business</p>

			<p>IEC is one of the most important documents required to gain growth prospects via import/export.</p>

			<p>The business has an option for online international marketplaces like Amazon.com, Alibaba, etc.</p>

			<p>Export Grants/subsidies</p>

			<p>1. The exporter can get a refund of many taxes paid while exporting the goods.</p>
			<p>2. The government of India trails a policy of exporting the goods and not the taxes.</p>
			<p>3. The government of India waives some import duties on goods if the importer exports the goods within a certain time frame.</p>
			
			<p class="black">Validity for Lifetime</p>

			<p>1. As mention earlier Import Export Code does not require renewal this is for a lifetime.</p>
			<p>2. The code only expires in case of winding up of the company or else it will be valid till the entity for which it is issued exists.</p>
			
			<p>Does not required return filing</p>

			<p>1. No compliance requirement for IEC</p>
			<p>2. Including this, periodical returns are no need to file.</p>
			<p>3. It is just like a PAN card.</p>
			
			<p>Individuals can have IE Code</p>

			<p>Any business can get IE Code in their business name also individuals who are proprietors can get IE Code on their name. It is not mandated to include a business entity for getting IE Code.</p>

			<p class="black">RCMC Registration</p>

			<p>Any Business or individual having import-export code can also apply for and get RCMC registration. Registration-Cum-Membership Certificate (RCMC) is a certificate that authenticates an exporter trading with products registered with an agency/ organisation that is authorised by the Indian Government. The certificate is valid for five years by the Export Promotional Councils which is under the commodity board in India. An exporter who needs to have this RCMC has to affirm his mainstream business in the application while applying for this. This request would be submitted to the Export Promotion Council/ Commodity Board concerning that line of business.</p>
