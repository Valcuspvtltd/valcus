            <h4><strong>12A Registration</strong></h4>
            <p>The concept of 12A registration is allotted by the Income Tax Department to trust organizations and other similar organization excluding profit organizations. The objective of the registration is to give exclusion from paying Income Tax. 12A registration is usually applied instantly after registration. NGOs, Trusts, and Section 8 that have incorporated 12A Registration get the benefit of exemption from income tax and other income. The 12A registration feature is accessible for all non-profit organizations. Therefore, it is mandatory for all Non-profit organizations, NGOs, and Trusts to get the information of 12A registration of the Income Tax Act.</p>
			
			<br/>
			
			<h4><strong>Eligibility for 12A Registration</strong></h4>
            
            <p>The entities that are looking for Section 12A registration must come under the non-profit purpose such as philanthropy, charity, and all that. In such an organization, their work can include social service, education, development for underprivileged children, women empowerment, and so on. The purpose of charitable and other social services of any organization is eligible to register under 12A registration.</p>
            
            <p>The first criteria for 12A registration are to verify whether any profit objective is indulged in the process obtained on by the applicant or entity. If there is no objective of earning profit and revenue, then the respective organization is eligible for 12A Registration.</p>
            
            <p>In case, the applicant or organization found to be involved in any profit or commercial activities the benefit under the section will be barred. In such circumstances, registration is approved solely if the receipts from the commercial activities are not exceeding 20 percent of the total revenue receipt of the applicant or organization.</p>
			<p>It is also kept in mind that 12A Registration is not valid for Family Trusts and Private firms. The purpose of the company or organization must revolve around the welfare of the public.</p>
			
			<br/>
			<h4><strong>Documents Needed for 12A Registration</strong></h4>
			
			<p>Yearly financial statement receipt for 3 previous financial years</p>
			<p>Note on the activities conducted by the entity</p>
			<p>Note on the activities performed by the organization or firm</p>
			<p>A self-attested copy of the documents that have proof of the acceptance or alteration of the goal of the organization needs to be filed. </p>
			<p>A self-attested copy of the equipment that was utilized to form the trust and set the entity should be filed.</p>
			
			<p>It is mandatory to file a self-attested copy of the registration that was formed with the authorized entity. The authorized entity might be the Societies or Registrar of Public Trusts, Registrar of Companies, and Registrar of Firms.</p>
			<p>The applicant firm might have earlier applied for the 12A registration process. In such a case, the application might be discarded.  A self-attested copy of the order of the cancellation should be enclosed with the application. </p>
			<p>In many cases, The Income Tax Department might reject the registration approved under this section.  After correcting the default, the applicant firm is permitted to apply the following application. In such circumstances, it is mandatory to file a self-attested copy of the order approving the registration.</p>
			
