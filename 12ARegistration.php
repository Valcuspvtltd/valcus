			<p>The applicant firm needs to file the application in an appropriate form online. Post the submission of 12A registration, the commissioner might ask the applicant to submit other subsequent documents and details as per requirements. The commissioner may ask for such documents to ensure the authenticity of the activities performed by the Trust in front of the Income Tax Department.  </p>
			<p>The commissioner might be contented with the 12A registration application. In such circumstances, the commissioner will give an approval letter mentioning that the applicant is eligible for 12A registration.  The written approval is given to the applicant and the applicant can get the benefits of 12A registration.</p>
			<p>The commissioner might not be contented with the application. In such a scenario, it is the sole right of the commissioner to cancel the application. The applicant should be conveyed in the writing of the cause of the registration cancellation.</p>    
