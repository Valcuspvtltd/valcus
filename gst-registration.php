			<br/>
			<p class="black">DIFFERENCE BETWEEN CGST VS SGST VS IGST</p>
			<br/>
			<p class="black">Central GST (CGST)</p>
			<p>When the supply of goods and services takes place WITHIN the State (Intra-state), that is when the Supplier and the Consumer both are in the same state, two taxes will be levied. One part is levied by the State Government and the other is by Central Government. Hence the tax levied by the central government is known as Central GST.</p>
			<br/>
			<p class="black">State GST (SGST)</p>
			<p>State GST is the component of GST that will be collected by the state government on all items, both goods, and services on intra-state trade.</p>
			
			<br/>
			<p class="black">Integrated GST (IGST)</p>
			<p>When the supply of goods and services takes place BETWEEN two States (Inter-state), that is when the place of Supplier and the Consumer are in different states, Integrated GST is levied by the central government. In the case of Inter-State supply of Goods and Services, only IGST will be applicable.</p>
