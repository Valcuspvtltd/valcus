			<p>Trade license is a certificate or a document issued by the municipal corporation for the people who wish to start their own business or trade in a particular area or a location, this license allows a person to carry out business or trade operations within the local limits of a particular region. However, such a certificate does not provide any right of ownership over the property neither it allows to commence any other business or trade other than that for which the certificate has been issued. Trade licenses are issued in order to regulate the businesses in an effective manner and to ensure that the activities of the business do not cause any health-related hazard to anyone. Every state requires a business to issue such a certificate at least 30 days before the commencement of the business and the certificate must be renewed periodically once obtained.</p>
			
			<p>The different types of trade licenses are shop licenses, industry licenses, and food and establishment licenses. The rules and regulations prescribe the eligibility criteria for trade license which states that the applicant must have attained the age of 18 years, the business or trade to be carried out must be legally permissible and the applicant must not have any criminal records. Throwing some light on the fee structure, it can be stated that these licenses are not for free, a certain amount of fee is required for a trade license, it can either be:</p>
			
			
			<ul>
			   <li>•    A fixed onetime fee to be paid at the time of application and renewal of the license or,</li>
