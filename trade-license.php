			</ul>
			
			<p>The trade license issued by the concerned authorities is valid for a period of one year and is required to be renewed annually. The application for the renewal of the license must be submitted within 30 days from the date of the expiry of the license. Although the application for trade license must be filed within 30 days from the commencement of the business, the processing time varies from place to another, i.e. in metro cities, it takes around 7-10 days while in other cities it can take longer than usual.</p>
			
			<p>This whole idea of a trade license is to ensure that no illegal activity is carried out within the environment and maintaining a healthy environment. In case of non-registration of trade license or non- fulfillment of any prerequisites of trade license, the person in violation will be liable to pay the penalty, and the authorities are allowed to take legal action against him.</p>
			
