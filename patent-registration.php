			<h4><strong>PATENT REGISTRATION</strong></h4>
			<p>A patent is an exclusive right granted to an individual to protect his or her invention from being made or used in any manner without the permission of the inventor for a limited period of time. The invention can be a new product or a new process or any technical solution related to a problem or any improvisations in the same thereof, basically getting one’s invention patented a person can prevent others from using the same for their own commercial gain.</p>
			
			<p>It is considered obligatory for all the Indian inventors who wish to protect their intellectual rights to get their patent registered as per the Patent Act 2002. The registration of the patent includes some specific documentation and a stepwise procedure as per the specified act. The registration process of the same has been mentioned below. Before filing an application regarding the registration of the patent, one must conduct a patentability search so as to know whether a patent is available for a particular invention or not.</p>
			 
			 
			 <br/>
			 
			 <h4><strong>What Can Be Patented in India </strong></h4>
    			<p> Firstly, the invention made must be new which has not been carried out or invented priority by any person and is not disclosed to the public in any manner,</p>
    	        <p>Secondly, the invention made shall be an inventive step which is different from the ordinary, or we can say that it must not be obvious.</p>
    	        <p>Thirdly, the invention made thereof should be capable of being used by the industry, in other words, it must possess the capability of industrial application.</p>
                <p>Fourthly, the application must be made before making any public disclosure that is it must not be known to the public or used by the public in any form.</p>
                <p>Fifthly, the last requirement being the invention must be of “patentable subject matter “under the applicable law.</p>

