		 <p class="black">GST (Goods & Service Tax)</p>
			<p>“GST is a cure for ills of existing Indirect Tax”</p>
			
			<p>Goods and Services Tax is an indirect tax levied in India on the supply of goods and services. GST is levied at every step in the production process but is meant to be refunded to all parties in the various stages of production other than the final consumer.</p>

			<p>Goods and Service tax means a tax on supply of goods or services, or both, except taxes on the supply of alcoholic liquor for human consumption (Article 366(12A) of Constitution of India). GST is a value-added tax levy sale or services or both. GST is a destination based consumption tax. GST offers a comprehensive and continuous chain of tax credit. GST where the burden is borne by the final consumer. GST eliminate cascading of tax. GST brings a uniform tax structure all over India.</p>
			
			<p>There are some Advantages in GST:-</p>
			
			<ol>
			<li>One Nation One Tax.</li>
			<li>Removal of bundled indirect taxes such as VAT, CST, Service Tax, CAD, SAD, and Excise.</li>
			<li>Removal of cascading effect of taxes i.e. removes the tax on tax.</li>
			<li>Increased ease of doing business.</li>
			<li>Lower cost of production, increase in demand will lead to an increase in supply. Hence, this will ultimately lead to rising in the production of goods. Resultant boost to make in India initiative.</li>
			<li>It will boost export and manufacturing activity, generate more employment and thus increase GDP with gainful employment leading to substantive economic growth.</li>
			</ol>
			
