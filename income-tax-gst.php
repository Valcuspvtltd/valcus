		<p class="black">Income Tax Return Filing</p>
			<p>There are seven types of Income Tax returns</p>
			
			<br/>
			<p class="black">ITR-1</p>
			
			<p>This Return Form is to be used by an individual whose total income for the assessment year 2018-19 includes:-</p>
			
			<ol>
			<li>Income from Salary/Pension or</li>
			<li>Income from One House Property&nbsp;(excluding cases where the loss is brought forward from previous years) or</li>
			<li>Income from Other Sources (Excluding Winning from Lottery and Income from Race Horses)</li>
			</ol>
			<br/>
			<p class="black">ITR-2</p>
			
			<p>This is to be used by an individual or a Hindu Undivided Family whose total income for the&nbsp;AY 2018-19 includes:-</p>
			
			<ol>
			<li>-Income from Salary/Pension; or</li>
			<li>-Income from House Property; or</li>
			<li>-Income from Other Sources (including Winnings from Lottery and Income from Race Horses).</li>
			</ol>
			
			<p>(Total income from the above should be more than Rs 50 Lakhs)</p>
			<ol>
			<li>Income from Capital Gains; or</li>
			<li>Income of a person as a Partner in the Firm</li>
			<li>Foreign Assets/Foreign income</li>
			<li>Agricultural income more than Rs 5,000</li>
			</ol>
			
			
			<p>Further, in a case where the income of another person like one’s spouse, child, etc. is to be clubbed with the income of the assesses, this Return Form can be used where such income falls in any of the above categories.</p>
			<br/>
			<p class="black">ITR-3</p>
			
			<p>The Current ITR 3 Form is to be used by an individual or a Hindu Undivided Family who have income from proprietary business or are carrying on the profession. The persons having income from the following sources are eligible to file ITR 3 :</p>
			
			
			<ol>
			<li>Carrying on a business or profession</li>
			<li>The return may include income from House property, Salary/Pension, and Income from other sources</li>
			</ol>
			<br/>
			<p class="black">ITR-4</p>
			<p>The current ITR4 is applicable to individuals and HUFs having income from a business or profession and who have opted for the presumptive income scheme as per section 44AD, Section 44ADA and Section 44AE of Income Tax Act. However, if the turnover of the business exceeds Rs. 2 cores, the taxpayer will have to file ITR-3.</p>
			
			<br/>
			<p class="black">ITR-5</p>
			<p>ITR 5 is for firms, LLPs (Limited Liability Partnership), AOPs (Association of Persons) and BOIs (Body of Individuals)</p>
			<br/>
			<p class="black">ITR-6</p>
			<p>For Companies other than companies claiming exemption under section 11 (Income from property held for charitable or religious purposes)</p>
			
			
			<p>This return has to be filed electronically only.</p>
			<br/>
			<p class="black">ITR-7</p>
			
			<p>For persons including companies required to furnish return under section 139(4A) or section 139(4B) or section 139(4C) or section 139(4D).</p>
			
			<ol>
			<li>Return under section 139(4A) is required to be filed by every person in receipt of income derived from property held under trust or other legal obligation wholly for charitable or religious purposes or in part only for such purposes.</li>
			<li>Return under section 139(4B) is required to be filed by a political party if the total income without giving effect to the provisions of section 139A exceeds the maximum amount which is not chargeable to income-tax.</li>
			<li>Return under section 139(4C) is required to be filed by every –
			<ul style="list-style:square;">
				<li>Scientific research association</li>
				<li>News agency</li>
				<li>Association or institution referred to in section 10(23A)</li>
				<li>Institution referred to in section 10(23B)</li>
				<li>Fund or institution or university or other educational institution or any hospital or other medical institution.</li>
			</ul>
			</li>
			<li>Return under section 139(4D) is required to be filed by every university, college or other institution, which is not required to furnish the return of income or loss under any other provision of this section.</li>
			</ol>
			
			<br/>
			<p class="black">GST (Goods & Service Tax)</p>
			<p>“GST is a cure for ills of existing Indirect Tax”</p>
			
			<p>Goods and Services Tax is an indirect tax levied in India on the supply of goods and services. GST is levied at every step in the production process but is meant to be refunded to all parties in the various stages of production other than the final consumer.</p>

			<p>Goods and Service tax means a tax on supply of goods or services, or both, except taxes on the supply of alcoholic liquor for human consumption (Article 366(12A) of Constitution of India). GST is a value-added tax levy sale or services or both. GST is a destination based consumption tax. GST offers a comprehensive and continuous chain of tax credit. GST where the burden is borne by the final consumer. GST eliminate cascading of tax. GST brings a uniform tax structure all over India.</p>
			
			<p>There are some Advantages in GST:-</p>
			
			<ol>
			<li>One Nation One Tax.</li>
			<li>Removal of bundled indirect taxes such as VAT, CST, Service Tax, CAD, SAD, and Excise.</li>
			<li>Removal of cascading effect of taxes i.e. removes the tax on tax.</li>
			<li>Increased ease of doing business.</li>
			<li>Lower cost of production, increase in demand will lead to an increase in supply. Hence, this will ultimately lead to rising in the production of goods. Resultant boost to make in India initiative.</li>
			<li>It will boost export and manufacturing activity, generate more employment and thus increase GDP with gainful employment leading to substantive economic growth.</li>
			</ol>
			
