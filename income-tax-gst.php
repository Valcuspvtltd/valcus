			<p class="black">Income tax & GST</p>
			<br/>
			
			<p>“Taxes are the due we pay for the privileges of membership in an organized Society.”</p>
			<br/>
			
			<p class="black">TAXES</p>
			<p class="black">Direct Taxes and Indirect Taxes</p>
			<p>The Tax we pay as an Income Tax is a Direct Tax, in which we are paying the tax and we are the sufferer of Tax. Income Tax is a certain percentage of our Income that we have to pay regularly to the Government. The Income Tax is Govern by THE INCOME TAX ACT, 1961. The Central Board of Direct Taxes (CBDT) is an important part of the Department of revenue. In Income tax, Income of Previous Year is assessed or taxed in succeeding year i.e., The Income of the Year 2016-17 is taxed or assessed in the Year 2017-18.</p>
			<p>A person by whom any tax or any other sum of money is payable under the Income Tax Act is the Assesses.  So, now the question is what is the definition of Person in the ACT?</p>
			<p>Which is very simple the following seven types of units of assessment</p>
			<ol>
			<li>An Individual</li>
			<li>A Hindu Undivided Family (HUF)</li>
			<li>A Company</li>
			<li>A firm</li>
			<li>An association of person or body of Individuals or not.</li>
			<li>A local authority</li>
			<li>Every artificial juridical person, not falling within any of the above categories.</li>
			</ol>
			<br/>
			<p class="black">Now the question is “What is the meaning of Income?”</p>
			<p>According to the definition given u/s 2(24), Income includes:-</p>
			<ol>
			<li>Profit and gain of business or profession.</li>
			<li>Dividend</li>
			<li>Voluntary contributions received by a Charitable Trust/Religious Trust/University/Educational Institution/Hospitals.</li>
			<li>The value of any perquisite of profit in lieu of Salary taxable u/s 17 &amp; any special allowance or benefit specifically granted either to meet personal expenses or for a performance of duties of an office or employment of profit.</li>
			<li>The value of any benefit or perquisite, whether convertible into money or not, received by a director or any of his relatives including the sum paid by the company which otherwise would have been payable by those people.</li>
			<li>The value of any benefit or perquisite, whether convertible into money or not, obtained by a representative assesses or by any person on whose behalf or for whose benefit any income is received by the representative assesses including any sum paid by the representative assesses, which would otherwise have been payable by the beneficiary.</li>
			<li>Any sum is chargeable to Income Tax under sub-clause of section 28, Section 41 or Section 5a</li>
			<li>Any capital gain chargeable under section 45.</li>
			<li>The profit and gain of business of insurance carried on by a mutual insurance company or by a Co-Operative Society computed in accordance with Section 44. Etc.</li>
			</ol>
