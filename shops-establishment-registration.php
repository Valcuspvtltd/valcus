			<p>With the increasing amount of businesses around the globe, a lot of people wish to enter into this promising world of profits and losses, any business be it small or big have to observe the provisions of a number of acts which regulates the businesses in general, one them being the Shop and Establishment Act which mandates every business working either on a small or a large scale to get registered under the Act.  The Shop and Establishment Act comes under the state legislation and every state has its own rules and regulations and a business needs to observe the rules and regulations of the state in which the registration is to be obtained. Basically, all shops, restaurants, residential hotels, theatres, clubs and other places of public amusements or “commercial establishments” comes under the provisions of the said Act except those which falls under the Factories Act, if a person chooses to register their commercial establishments under the said act he or she should visit the official website or the state government office to complete the registration process following the method prescribed by the state government.  Under the Act such a “commercial establishment” includes:</p>
				


			<ul>
		        <li>•	A society registered under SRA 1860</li>
		        <li>•	Contractors and auditors</li>
                <li> •	Restaurants and cafes</li>
                <li> •	Hotels and clubs</li>
                <li> •	Private educational institutions</li>
                <li> •	Banking, insurance, etc</li>
                <li> •	Any other entertainment businesses</li>
		    </ul>
		    
		    
		    <p>Every such establishment is required to register itself under the said Act within 30 days of commencement of business, though this serves as a basic license many other licenses are also required as a proof of a commercial business. The objectives of such an Act are to safeguard the rights of the employees of the unorganized sectors and to ensure that they operate in a just environment and receive adequate payment and fair treatment.</p>
		    
		    <p>The regulations, prescribed fees and the timings of such an establishment may differ depending upon one state to another, the registration process has been mentioned below:</p>
		    
		    
		    <ul>
    		    <li>•	The employer should send an application to the inspector in charge, i.e., to the district labor officer, within 30 days of the commencement of the establishment along with the prescribed fees.</li>
                <li>•	The application should contain details like the name of the employer and the establishment, address of the establishment, type of establishment, and the date on which the establishment started operating.</li>
                <li>•	The shop and establishment act renders the authority to the labour department of each state for looking after the registration process.</li>
                <li>•	On being satisfied, the inspector may grant the registration certificate to the employer such a certificate must be displayed by the employer at such a shop or the establishment. This whole process takes about 10 to 20 days including the issuance of the certificate.</li>
            </ul>


