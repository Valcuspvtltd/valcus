		  <p>If the turnover of the taxpayer (irrespective of its category) is equivalent to or more than Rs. 1 Crore from the business or Rs. 50 Lac from the income from profession then according to The Income Tax Act, 1961 an audit known as Tax Audit by a practicing chartered accountant (CA) is required to be done under section 44AB. The tax audit report must be filed online at the income tax portal before 30th September of the assessment year. Tax Audit is an independent audit by a chartered accountant in full-time practice concerning the matters related to Taxation only and a report confirming that there is no concealment of income by the taxpayer and that there is no non-payment of tax liability and the same has is paid on due dates. The tax audit is a statutory obligation on the part of the taxpayer and is applicable on all cases where the turnover or the gross receipts during the last year is more than the limit prescribed under section 44AB for the respective assessment year. The due date for filing the tax audit report is 30th September of the assessment year. If the audit report is not submitted within its due date then the taxpayer is required to pay a penalty of an amount equal to 1.5% of the gross receipts/turnover, however, subject to a maximum fine of Rs. 1.5 lac.</p>
